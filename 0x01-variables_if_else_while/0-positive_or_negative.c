#include <stdlib.h>
#include <time.h>
#include <stdio.h>

/**
* main - this function generates random number n
* and checks whether it is positive or negative
* Return: return is 0
*/
int main(void)
{
	int n;

	srand(time(0));
	n = rand() - RAND_MAX / 2;
	if (n > 0)
	{
		printf("%d is positive\n", n);
	}
	else if (n == 0)
	{
		printf("%d is zero\n", n);
	}
	else
	{
		printf("%d is negative\n", n);
	}
	return (0);
}
